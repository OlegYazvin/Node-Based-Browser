#!/usr/bin/env node

import { writeFileSync } from "node:fs";
import path from "node:path";
import process from "node:process";
import { fileURLToPath } from "node:url";

const scriptDirectory = path.dirname(fileURLToPath(import.meta.url));
const geckoRoot = path.resolve(scriptDirectory, "..");
const repositoryRoot = path.resolve(geckoRoot, "..");

function parseArguments(argv) {
  const options = {
    checkoutDir: path.resolve(repositoryRoot, "..", "Nodely-Gecko", "firefox-esr"),
    output: "mozconfig.nodely",
    mode: "artifact"
  };

  for (let index = 0; index < argv.length; index += 1) {
    const argument = argv[index];

    switch (argument) {
      case "--checkout-dir":
      case "--firefox-dir":
        options.checkoutDir = path.resolve(argv[++index]);
        break;
      case "--output":
        options.output = argv[++index];
        break;
      case "--mode":
        options.mode = argv[++index];
        break;
      case "--help":
        console.log("Usage: node gecko/scripts/write-mozconfig.mjs [--checkout-dir <path>] [--output <filename>] [--mode artifact|full]");
        process.exit(0);
        break;
      default:
        throw new Error(`Unknown argument: ${argument}`);
    }
  }

  return options;
}

function buildMozconfig({ mode }) {
  const lines = [
    "# Autogenerated by gecko/scripts/write-mozconfig.mjs",
    "mk_add_options MOZ_OBJDIR=@TOPSRCDIR@/obj-nodely",
    'ac_add_options --enable-application=browser',
    'ac_add_options --disable-debug'
  ];

  if (mode === "artifact") {
    lines.push(
      "# Keep artifact builds close to upstream Firefox; refresh-artifact-branding.mjs rewrites the user-facing Nodely identity after build/package."
    );
    lines.push('. "$topsrcdir/build/mozconfig.artifact"');
  } else {
    lines.push('ac_add_options --with-app-name=nodely');
    lines.push('ac_add_options --with-app-basename=Nodely');
    lines.push('export MOZ_APP_VENDOR=Nodely');
    lines.push('export MOZ_APP_DISPLAYNAME=Nodely');
    lines.push('export MOZ_APP_REMOTINGNAME=nodely');
    lines.push('export MOZ_MACBUNDLE_ID=org.nodely.browser');
    lines.push('export MOZ_CRASHREPORTER_URL=https://crashes.nodely.invalid/submit');
  }

  lines.push("");
  return `${lines.join("\n")}\n`;
}

try {
  const options = parseArguments(process.argv.slice(2));
  const targetPath = path.join(options.checkoutDir, options.output);
  writeFileSync(targetPath, buildMozconfig(options), "utf8");
  console.log(`Wrote ${targetPath}`);
} catch (error) {
  console.error(error instanceof Error ? error.message : String(error));
  process.exit(1);
}
